/**
 * Fixed French expressions the reading view recognises as one unit: tapping any
 * word inside one looks up the whole phrase (« grâce à » → "thanks to"), not
 * the word alone. Curated for the A1–B2 corpus — the list was tuned against
 * every n-gram in `articles_corpus.json`, then rounded out with the standard
 * learner locutions so future articles are covered too.
 *
 * `gloss` is the first-line translation, already in the app's template form
 * (verbs "to …", lowercase, first sense only — see normalizeGloss). It shows
 * instantly and works offline; the online dictionary still fills the longer
 * definition lines.
 *
 * Matching (see lib/expressions.ts) compares each token against the sentence
 * word's surface OR its lemma, so writing a verb as an infinitive matches every
 * conjugated form: « avoir besoin de » matches « a besoin de », « ont besoin
 * de »… Elisions match through their lemma (« parce qu' » → parce que) and
 * de/à match their contractions (« grâce aux », « près du »). `match` overrides
 * the tokens only when the canonical spelling isn't what should be matched
 * (« il s'agit de » must match through the lemma agir).
 */
export interface ExpressionEntry {
  /** Canonical form — shown in the modal and stored as the lexicon lemma. */
  expr: string;
  /** Curated first-line translation ('' never allowed — curate or omit the entry). */
  gloss: string;
  /** Normalized match tokens; defaults to the tokenized `expr`. */
  match?: string[];
}

